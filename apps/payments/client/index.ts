import axios from "axios";
import {
  PaymentRequestsApi,
  ProvidersApi,
  TransactionsApi,
} from "./autogenerated";
import { getAuthCookie } from "../libraries/auth";

const buildAxiosInstance = (userId, authToken?: string) => {
  const instance = axios.create({});
  instance.defaults.headers.common["x-user-id"] = userId;
  instance.defaults.headers.common["x-logto-auth"] = getAuthCookie();

  if (authToken) {
    instance.defaults.headers.common["Authorization"] = `Bearer ${authToken}`;
  }

  return instance;
};

export default (userId, authToken?: string) => {
  return {
    providers: new ProvidersApi(
      undefined,
      process.env.BACKEND_URL,
      buildAxiosInstance(userId, authToken),
    ),
    paymentRequests: new PaymentRequestsApi(
      undefined,
      process.env.BACKEND_URL,
      buildAxiosInstance(userId, authToken),
    ),
    transactions: new TransactionsApi(
      undefined,
      process.env.BACKEND_URL,
      buildAxiosInstance(userId),
    ),
  };
};
